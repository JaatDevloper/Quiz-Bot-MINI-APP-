// server/utils.ts

/**
 * Provides a basic logging function, isolated from development tools like Vite.
 * This function is safe to be included in the production build.
 */
export function log(message: string, source = "express") {
  const formattedTime = new Date().toLocaleTimeString("en-US", {
    hour: "numeric",
    minute: "2-digit",
    second: "2-digit",
    hour12: true,
  });

  console.log(`${formattedTime} [${source}] ${message}`);
}
